			( HPC_USER_HPSEL1_SELECT7 << _HPSEL1_SELECT7_POSITION ) ;
		#ifdef DEF_BITNAME
			HPSEL1bits.SELECT4 = HPC_USER_HPSEL1_SELECT4;
			HPSEL1bits.SELECT5 = HPC_USER_HPSEL1_SELECT5;
			HPSEL1bits.SELECT6 = HPC_USER_HPSEL1_SELECT6;
			HPSEL1bits.SELECT7 = HPC_USER_HPSEL1_SELECT7;
		#endif 

	/*-----------------------------------------------------------------------*/
	/* HPCCNTL0レジスタ */
	/*-----------------------------------------------------------------------*/
		HPCCNTL0 = HPC_USER_HPCCNTL0;
	/*-----------------------------------------------------------------------*/
	/* HPCCNTH0レジスタ */
	/*-----------------------------------------------------------------------*/
		HPCCNTH0 = HPC_USER_HPCCNTH0;
	/*-----------------------------------------------------------------------*/
	/* HPCCNTL1レジスタ */
	/*-----------------------------------------------------------------------*/
		HPCCNTL1 = HPC_USER_HPCCNTL1;
	/*-----------------------------------------------------------------------*/
	/* HPCCNTH1レジスタ */
	/*-----------------------------------------------------------------------*/
		HPCCNTH1 = HPC_USER_HPCCNTH1;
	/*-----------------------------------------------------------------------*/
	/* HPCCNTL2レジスタ */
	/*-----------------------------------------------------------------------*/
		HPCCNTL2 = HPC_USER_HPCCNTL2;
	/*-----------------------------------------------------------------------*/
	/* HPCCNTH2レジスタ */
	/*-----------------------------------------------------------------------*/
		HPCCNTH2 = HPC_USER_HPCCNTH2;
	/*-----------------------------------------------------------------------*/
	/* HPCCNTL3レジスタ */
	/*-----------------------------------------------------------------------*/
		HPCCNTL3 = HPC_USER_HPCCNTL3;
	/*-----------------------------------------------------------------------*/
	/* HPCCNTH3レジスタ */
	/*-----------------------------------------------------------------------*/
		HPCCNTH3 = HPC_USER_HPCCNTH3;
	/*-----------------------------------------------------------------------*/
	/* HPCCNTL4レジスタ */
	/*-----------------------------------------------------------------------*/
		HPCCNTL4 = HPC_USER_HPCCNTL4;
	/*-----------------------------------------------------------------------*/
	/* HPCCNTH4レジスタ */
	/*-----------------------------------------------------------------------*/
