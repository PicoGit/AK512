/*
* @fn        vdg_CAN1_Set_Register(void) 
* @brief        CAN1レジスタを初期化します 
* @param
* @return
* @detail
*/
/*----------------------------------------------------------------------------*/
void vdg_CAN1_Init_Register(void)
{
    /*----------------------------------------------------------------------------*/
    /* モジュールの有効化 */
    /*----------------------------------------------------------------------------*/
        C1CONbits.ON= 1u;
    /*----------------------------------------------------------------------------*/
    /* コンフィグレーションモードに移行 */
    /*----------------------------------------------------------------------------*/
        C1CONbits.REQOP = 4u;
        while(C1CONbits.OPMOD != 4u);
    /*----------------------------------------------------------------------------*/
    /* 送受信バッファの設定 */
    /*----------------------------------------------------------------------------*/
        C1FIFOBA = (unsigned int) &CanMessageBuffer;
    /*----------------------------------------------------------------------------*/
    /* CANFDモジュールを1Mbpsの公称ビットレート速度と2Mbpsのデータビット速度に設定します。 */
    /*----------------------------------------------------------------------------*/
        /* Set up the CANFD module for 1Mbps of Nominal bit rate speed and 2Mbps of Data bit rate. */
#if 0 
        C1NBTCFGH = 0x003E;
        C1NBTCFGL = 0x0F0F;
        C1DBTCFGH = 0x001E;
        C1DBTCFGL = 0x0707;
        C1TDCH = 0x0002; //TDCMOD is Auto
        C1TDCL = 0x1F00;
#else
        C1NBTCFGbits.BRP = CAN_USER_C1NBTCFG_BRP;       /* TQ = 2/FSYS*/
        C1NBTCFGbits.TSEG1 = CAN_USER_C1NBTCFG_TSEG1;	/* TSEG1 = 143TQ */
        C1NBTCFGbits.TSEG2 = CAN_USER_C1NBTCFG_TSEG2;	/* TESG2 = 36TQ */
        C1NBTCFGbits.SJW = CAN_USER_C1NBTCFG_SJW;		/* SJW = 36TQ */
		C1TDCbits.TDCMOD = CAN_USER_C1TDC_TDCMOD;
		C1TDCbits.EDGFLTEN = CAN_USER_C1TDC_EDGFLTEN;
		C1TDCbits.SID11EN = CAN_USER_C1TDC_SID11EN;
		C1TDCbits.TDCO = CAN_USER_C1TDC_TDCO;
		C1TDCbits.TDCV = CAN_USER_C1TDC_TDCV;

#endif
    /*----------------------------------------------------------------------------*/
    /*【CAN*】送信キューとBRSを有効にするようにCANFDモジュールを設定します*/
    /*----------------------------------------------------------------------------*/    
        /* Configure CANFD module to enable Transmit Queue and BRS*/
#if (CAN1_FRAME_MODE == CAN_FRAME_20)
        C1CONbits.BRSDIS = 0x1u;
#else
        C1CONLbits.BRSDIS = 0x0u;
#endif
        C1CONbits.STEF = 0x0; //Don't save transmitted messages in TEF
        C1CONbits.TXQEN = 0x1;
    /*----------------------------------------------------------------------------*/
    /*【CAN*】TXQ設定*/
    /*----------------------------------------------------------------------------*/    
        /* Configure TXQ to transmit 1 message*/
        C1TXQCONbits.FSIZE = CAN1_TXQ_DEEP - 1u; // single message
        C1TXQCONbits.PLSIZE = CAN1_PAYLOAD_SIZE; // 8 bytes of data
    /*----------------------------------------------------------------------------*/
    /*【CAN*】FIFO1設定(送信)*/
    /*----------------------------------------------------------------------------*/    
        /* Configure FIFO1 to transmit 2 messages*/
        C1FIFOCON1bits.FSIZE = CAN1_FIFO_DEEP - 1u; //2 messages
        C1FIFOCON1bits.PLSIZE = CAN1_PAYLOAD_SIZE; //8 bytes of data
        C1FIFOCON1bits.TXEN = 0x0; // Set TXEN bit ,transmit fifo
    /*----------------------------------------------------------------------------*/
    /*【CAN*】FIFO2設定(受信)*/
    /*----------------------------------------------------------------------------*/    
        C1FIFOCON1bits.FSIZE = CAN1_FIFO_DEEP - 1u; //2 messages
        C1FIFOCON1bits.PLSIZE = CAN1_PAYLOAD_SIZE; //64 bytes of data
        C1FIFOCON1bits.TXEN = 0x0; // Set TXEN bit ,transmit fifo
        
    /*----------------------------------------------------------------------------*/
    /*【CAN*】Filter設定*/
    /*----------------------------------------------------------------------------*/ 
        u2g_CAN1_Init_FilterObj();
        
    /*----------------------------------------------------------------------------*/
    /* ノーマルモードに移行 */
    /*----------------------------------------------------------------------------*/
        C1CONbits.REQOP = 0;
        while(C1CONbits.OPMOD != 0); 
}

